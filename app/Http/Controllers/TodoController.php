     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(TodoRequest $request)
    {   
        $todo = Todo::create(array_merge($request->all(),['user_id' => auth()->user()->id]));

        return response()->json('true', 201);
